#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xE0,0xA3,0x1F,0x63,0xA9,0xD9,0x96,0xF1,0xF8,0x44,0x37,0x27,
		0x4A,0xCB,0xC5,0xDD,0x64,0x9C,0x1D,0xD1,0xF1,0xC2,0x3E,0xE5,
		0x3D,0xAC,0x19,0x45,0x7D,0xE7,0xED,0x94,0xA9,0xE4,0x69,0x5E,
		0x1D,0xC2,0xAC,0x52,0xB5,0x55,0xDD,0x77,0x49,0xA7,0x17,0x11,
		0xE2,0x1E,0xE6,0x3D,0x34,0xB2,0x16,0xB2,0x15,0x32,0x4F,0xC4,
		0x71,0x53,0x15,0x1B,
		};
	static unsigned char dh512_g[]={
		0x05,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
